# 字符串格式化
# 1.保留小数点后两位
print("{:.2f}".format(3.1415926))
# 2. 带符号保留小数点后2位
print("{:+.2f}".format(-1))
# 3.不带小数位
print("{:.0f}".format(2.718))
# 4. 整数补0，填充左边，宽度为3
print("{:0>3d}".format(5))
# >>> 005
# 5. 以逗号分隔的数字格式（按照美式分隔，以1000为分隔）
print("{:,}".format(10241024))
# >>> 10,241,024
# 6. 百分比格式
print("{:.2%}".format(0.718))
# >>> 71.8%
# 7.指数记法
print("{:.2e}".format(10241024))
# >>> 1.02e+07
