# "" 双引号下的特殊字符不需要转移
# 例
print("That isn't a horse")
# 单引号需要转义
print('That isn\'t a horse')
# ord函数将单个字符（字符！） 转化为整数
# chr函数将整数转化为字符

# bytes函数将字符串（字符串！）转化为字节类型（bytes）
# str函数将字节类型（bytes） 转化为字符串
s = "python"
